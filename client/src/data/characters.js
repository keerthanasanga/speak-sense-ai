const dedupeAvatars = (list = []) => {
  const seen = new Set();
  const deduplicated = [];

  for (const avatar of list) {
    // Use ID as primary unique key (most reliable)
    const id = avatar?.id ?? `${avatar?.name}-${avatar?.role}`;
    
    if (!seen.has(id)) {
      seen.add(id);
      deduplicated.push(avatar);
    }
  }

  return deduplicated;
};

const dedupeByVisualSignature = (list = []) => {
  const seenVisual = new Set();

  return list.filter((avatar) => {
    const visualKey = [
      avatar?.species || "human",
      avatar?.gender || "other",
      avatar?.hairStyle || "",
      avatar?.hairColor || "",
      avatar?.eyeColor || "",
      avatar?.avatar || "",
    ].join("|");

    if (seenVisual.has(visualKey)) return false;
    seenVisual.add(visualKey);
    return true;
  });
};

const avatarTypeKey = (avatar) => [
  avatar?.species || "human",
  avatar?.gender || "other",
].join("-");

const diversifyAvatarOrder = (list = []) => {
  const groups = {
    animal: [],
    female: [],
    male: [],
    other: [],
  };

  list.forEach((avatar) => {
    if (avatar?.species === "animal") {
      groups.animal.push(avatar);
      return;
    }

    if (avatar?.gender === "female") {
      groups.female.push(avatar);
      return;
    }

    if (avatar?.gender === "male") {
      groups.male.push(avatar);
      return;
    }

    groups.other.push(avatar);
  });

  const order = ["animal", "female", "male", "other"];
  const diversified = [];

  while (order.some((key) => groups[key].length > 0)) {
    const lastType = diversified.length ? avatarTypeKey(diversified[diversified.length - 1]) : null;

    const nextGroupKey = order.find((key) => {
      const candidate = groups[key][0];
      if (!candidate) return false;
      if (!lastType) return true;
      return avatarTypeKey(candidate) !== lastType;
    }) || order.find((key) => groups[key].length > 0);

    if (!nextGroupKey) break;

    const nextAvatar = groups[nextGroupKey].shift();
    if (!nextAvatar) break;
    diversified.push(nextAvatar);
  }

  return diversified;
};

export const getFilteredAvatars = (catalog, industry) => {
  const normalizedIndustry = (industry || "").toLowerCase();
  let merged = [];

  if (normalizedIndustry.includes("software") || normalizedIndustry.includes("technology") || normalizedIndustry.includes("it")) {
    merged = [...catalog.software, ...catalog.default.slice(3)];
  } else if (normalizedIndustry.includes("data") || normalizedIndustry.includes("ml") || normalizedIndustry.includes("machine")) {
    merged = [...catalog.data, ...catalog.default.slice(0, 3)];
  } else {
    merged = [...catalog.default];
  }

  return diversifyAvatarOrder(dedupeByVisualSignature(dedupeAvatars(merged)));
};

